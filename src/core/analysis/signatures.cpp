bool SignatureMatcher::match_one(const u8* data, size_t len, const Signature& sig) const {
    if (len < sig.pattern.size()) return false;
    for (size_t i = 0; i < sig.pattern.size(); ++i) {
        if (sig.mask[i] && data[i] != sig.pattern[i])
            return false;
    }
    return true;
}

const u8* SignatureMatcher::func_bytes(va_t addr, const PEImage& img, size_t* out_len) const {
    for (auto& seg : img.segments) {
        if (seg.contains(addr)) {
            size_t off = static_cast<size_t>(addr - seg.va);
            if (out_len) *out_len = seg.data.size() - off;
            return seg.data.data() + off;
        }
    }
    if (out_len) *out_len = 0;
    return nullptr;
}

int SignatureMatcher::match_functions(AnalysisDB& db, const PEImage& img) {
    int matches = 0;
    for (auto& [entry, func] : db.funcs) {
        size_t avail = 0;
        const u8* ptr = func_bytes(entry, img, &avail);
        if (!ptr || avail < 4) continue;

        size_t check_len = std::min<size_t>(avail, 64);
        for (auto& sig : sigs_) {
            if (match_one(ptr, check_len, sig)) {
                func.name = sig.name;
                db.names[entry] = sig.name;
                ++matches;
                break;
            }
        }
    }
    spdlog::info("signatures: matched {} functions", matches);
    return matches;
}

